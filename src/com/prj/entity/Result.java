package com.prj.entity;


public class Result {

  private int id;
  private int uid;
  private String title;
  private int result;
  private String time;
  private int mid;
  private String info;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public int getUid() {
    return uid;
  }

  public void setUid(int uid) {
    this.uid = uid;
  }


  public String getTitle() {
    return title;
  }

  public void setTitle(String title) {
    this.title = title;
  }


  public int getResult() {
    return result;
  }

  public void setResult(int result) {
    this.result = result;
  }


  public String getTime() {
    return time;
  }

  public void setTime(String time) {
    this.time = time;
  }


  public int getMid() {
    return mid;
  }

  public void setMid(int mid) {
    this.mid = mid;
  }


  public String getInfo() {
    return info;
  }

  public void setInfo(String info) {
    this.info = info;
  }

}
