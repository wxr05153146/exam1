package com.prj.entity;


public class History {

  private int id;
  private int uid;
  private int mid;
  private int score;
  private String time;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public int getUid() {
    return uid;
  }

  public void setUid(int uid) {
    this.uid = uid;
  }


  public int getMid() {
    return mid;
  }

  public void setMid(int mid) {
    this.mid = mid;
  }


  public int getScore() {
    return score;
  }

  public void setScore(int score) {
    this.score = score;
  }


  public String getTime() {
    return time;
  }

  public void setTime(String time) {
    this.time = time;
  }

}
